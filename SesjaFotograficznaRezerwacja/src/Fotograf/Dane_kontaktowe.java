package Fotograf;

public class Dane_kontaktowe {
	
	public String imie;
	public String nazwisko;
	public String email;
	public int wiek;
	public String adres;
	
	public Dane_kontaktowe()
	{
		
	}
	
	public Dane_kontaktowe(String imie, String nazwisko, String email, int wiek, String adres)
	{
		this.imie = imie;
		this.nazwisko = nazwisko;
		this.email = email;
		this.wiek = wiek;
		this.adres = adres;
	}

}
